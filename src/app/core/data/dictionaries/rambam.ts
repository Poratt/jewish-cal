// Helper function to create a normalized (lowercase key) map
function createNormalizedMap(map: Record<string, RambamHalacha>): Record<string, RambamHalacha> {
    const normalized: Record<string, RambamHalacha> = {};
    for (const key in map) {
        if (Object.prototype.hasOwnProperty.call(map, key)) {
            normalized[key.toLowerCase()] = map[key];
        }
    }
    return normalized;
}

